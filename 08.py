# Valor digitado pelo usuario com texto
numero = input("Digite um numero:")

# Exibe o valor lido
print("Valor lido:", numero)

# Exibe o tipo antes da conversão
print("Tipo antes:", type(numero))

# Converte o texto para numero inteiro 
numero =int(numero)

# Exibe o valor depois da conversão
print("Valor convertido:", numero)

# Exibe o tipo depois da conversão
print("Tipo depois:", type(numero))